#include "keytoktab.h"

/**********************************************************************/
/* OBJECT ATTRIBUTES FOR THIS OBJECT (C MODULE)                       */
/**********************************************************************/
#define NENTS 4

static int optab[][NENTS] = {
   {'+', integer, integer, integer},
   {'+', real,    real,    real},
   {'+', integer, real,    real},
   {'+', real,    integer, real},
   {'*', integer, integer, integer},
   {'*', real,    real,    real},
   {'*', integer, real,    real},
   {'*', real,    integer, real},
   {'$', undef,   undef,   undef}
};

/**********************************************************************/
/*  PRIVATE METHODS for this OBJECT  (using "static" in C)            */
/**********************************************************************/

/**********************************************************************/
/*  PUBLIC METHODS for this OBJECT  (EXPORTED)                        */
/**********************************************************************/
/**********************************************************************/
/* display the op tab                                                 */
/**********************************************************************/
static void p_row_optab(int i) {
   puts("\n");
   for (int j = 0; j < NENTS; j++) {
      printf("%-12s ", tok2lex(optab[i][j]));
   }
}

void p_optab() {
   char *headers[] = {"operator", "arg1", "arg2", "result"};
   printf("\n________________________________________________________");
   printf("\nTHE OPERATOR TABLE");
   printf("\n________________________________________________________\n");
   for (int i = 0; i < sizeof(headers) / sizeof(char*); i++) {
      printf("%-12s ", headers[i]);
   }
   printf("\n________________________________________________________");
   for (int i = 0; optab[i][0] != '$'; i++) {
      p_row_optab(i);
   }
   printf("\n________________________________________________________");
}

/**********************************************************************/
/* return the type of a binary expression op arg1 arg2                */
/**********************************************************************/
int get_otype(int op, int arg1, int arg2) {  
   for (int i = 0; optab[i][0] != '$'; i++) {
      if (optab[i][0] == op && optab[i][1] == arg1 && optab[i][2] == arg2) {
         return optab[i][3];
      }
   }
   return undef;
}

